package wtyw.worklite.beans;

public class WorkliteRules {
	
	private String rule_id;
	private String rule_detail;
	private String remark1;
	private String remark2;
	
	public String getRule_id() {
		return rule_id;
	}
	public void setRule_id(String ruleId) {
		rule_id = ruleId;
	}
	public String getRule_detail() {
		return rule_detail;
	}
	public void setRule_detail(String ruleDetail) {
		rule_detail = ruleDetail;
	}
	public String getRemark1() {
		return remark1;
	}
	public void setRemark1(String remark1) {
		this.remark1 = remark1;
	}
	public String getRemark2() {
		return remark2;
	}
	public void setRemark2(String remark2) {
		this.remark2 = remark2;
	}	
}
